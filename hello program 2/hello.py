# Variables and arithmetic operations
a = 5
b = 3
sum = a + b
difference = a - b
product = a * b
quotient = a / b

print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)

# Conditional statements
if a > b:
    print("a is greater than b")
elif a < b:
    print("a is less than b")
else:
    print("a is equal to b")

# Looping
for i in range(5):
    print("Iteration:", i)

# Functions
def greet(name):
    print("Hello,", name)

greet("Alice")
greet("Bob")
